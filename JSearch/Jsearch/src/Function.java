public class Function {
    String returnType;
    String[] params;
    String[] paramsNames;
    String name;

    public String getReturnType() {
        return returnType;
    }

    public String[] getParams() {
        return params;
    }

    public String[] getParamsNames() {
        return paramsNames;
    }

    public String getName() {
        return name;
    }
}
